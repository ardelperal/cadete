    <h3>
        04. Cambios en AGEDYS
    </h3>
</div>

<p>
    En la aplicación de AGEDYS dentro de la Lanzadera se han realizado las siguientes
    actualizaciones:
</p>

<ul>

    <li> 
        Se ha creado la opción de realizar una petición de proyecto en nombre de otro para
        el expediente OTROS-COMERCIALES. Por tanto, ahora le aparecerán las
        comunicaciones de ese Proyecto a ese otro usuario (Visados de facturas). 
    </li>

    <li> 
        Se ha añadido un nuevo indicador de tarea pendiente de Factura por visado
        técnico. Aparecen las facturas pendientes de visado de los expedientes en los que
        formas parte y los que pudiera haber en el expediente genérico OTROSCOMERCIALES.
    </li>

</ul>

