    <h2>
        01. ¿Qué es Quality Review?                         
    </h2>

</div>

<p> 
    Quality Review es la primera revista de calidad creada por y
    para el Departamento de Defensa de Telefónica.
</p>

<div class="centrado">
    <img src="/build/img/com/CDQ-22-013/1.jpg" style="height:400px">   
    <img src="/build/img/com/CDQ-22-013/2.jpg" style="height:400px">   
</div>


<p>
    Este nuevo método de comunicación
    permitirá trasladar toda la
    información de calidad actualizada
</p>

<p>
    Se pretende reducir la cantidad de
    información que se hacía llegar
    mediante los correos que acababan
    en la carpeta de spam.
</p>

<p>
    ¿Cuándo podrás ver cada edición
    de la revista?  
</p>


<ul class="list--dots">

    <li> La primera vez de cada mes que te conectes a la red desde la oficina. </li>
    <li> Aparecerá al iniciar el ordenador la revista de calidad de dicho mes. </li>
    <li> Tendrás todas las ediciones disponibles en la Web de Calidad.</li>

</ul>



<div class="centrado">

    <h2>
        02. Materialización de riesgos                      
    </h2>

</div>

<p>
    Si has accedido últimamente a la aplicación de Gestión de Riesgos de la Lanzadera,
    te habrás dado cuenta de que su formato ha cambiado bastante. Si aún no lo has
    hecho, te invito a que lo hagas cuanto antes.
</p>

<div class="centrado">
    <img src="/build/img/com/CDQ-22-013/3.jpg" style="height:300px">     
</div>

<ul class="list--dots">

    <li> 
        <img src="/build/img/com/CDQ-22-013/4.jpg" style="height:30px"> 
        Tareas pendientes: 
        Estas tareas indican los proyectos a tu cargo que se encuentran a punto de
        caducar o caducados y si hay riesgos aceptados, retirados o materializados. 
    </li>

    <li> 
        <img src="/build/img/com/CDQ-22-013/5.jpg" style="height:30px"> 
        Gestiones de Riesgo: 
        Podrás observar la gestión de riesgos de los diferentes proyectos a tu cargo,
        tal y como aparecía antes de la actualización.
    </li>

    <li> 
        <img src="/build/img/com/CDQ-22-013/6.jpg" style="height:30px"> 
        Nueva Gestión de Riesgo: 
        Te permite dar de alta una nueva gestión de riesgos para un proyecto.
    </li>

    <li> 
        <img src="/build/img/com/CDQ-22-013/7.jpg" style="height:30px"> 
        Biblioteca de Riesgos: 
        Como su propio nombre indica, tendrás acceso a una lista de riesgos de
        proyecto y de pedido que te ayudarán a identificar los riesgos de tu
        proyecto.
    </li>

    <li> 
        <img src="/build/img/com/CDQ-22-013/8.jpg" style="height:30px"> 
        Informe indicador: 
        Genera un informe con los riesgos de todos los proyectos, enumerando los
        riesgos identificados y los materializados.         
    </li>

    <li> 
        <img src="/build/img/com/CDQ-22-013/9.jpg" style="height:30px"> 
        Último proyecto:
        Como su propio nombre indica, identifica el último proyecto sobre
        el que hemos gestionado sus riesgos. 
    </li>

</ul>

<p>
    Como sabemos, el ciclo de vida de un riesgo se inicia cuando se detecta y finaliza
    cuando se cierra. En el camino de este ciclo de vida tenemos distintas etapas que
    pueden acontecer, entre ellas, la materialización de un riesgo.
</p>

<div class="centrado">
    <img src="/build/img/com/CDQ-22-013/10.jpg" style="height:300px">     
</div>

<p>
    Normalmente esta materialización
    implica un incumplimiento de los
    requisitos del proyecto, por ello se ha
    automatizado la apertura de una no
    conformidad asociada al riesgo cuando
    este se materialice.
</p>

<div class="centrado">
    <img src="/build/img/com/CDQ-22-013/11.jpg" style="height:80px">     
</div>

<p>
    Posteriormente el personal de calidad
    aceptará la apertura de la no
    conformidad, estableciendo acciones y un
    plazo limitado para el cierre de la
    misma.
</p>

<p>
    Si deseas conocer más información sobre la
    gestión de riesgos, puedes consultar el
    procedimiento <a href="#">EM-300-PR-009</a> , que
    encontrarás en la página web de calidad.
</p>

