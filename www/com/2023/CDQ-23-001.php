    <h2>
        02. Riesgos de proyectos                   
    </h2>

</div>

<p>
    Por orden del RAC, se ha modificado la frecuencia de
    Revisión de Evaluación de Riesgos de los proyectos.
</p>

<p>
    ¡Toca ponerse las pilas! Porque a partir de ahora, la
    evaluación de Riesgos de cada proyecto se deberá
    realizar una vez al mes.
</p>

<div class="centrado">
    <img src="/build/img/com/CDQ-23-001/3.jpg" style="height:300px">     
</div>

<p>
    Aprovecha para examinar a fondo los riesgos de tu
    proyecto, incluyendo los planes de mitigación y
    contigencia asociados. Esto permitirá que la revisión
    mensual sea mucho más ligera.
</p>

<div class="centrado">
    <img src="/build/img/com/CDQ-23-001/4.png" style="height:150px">     
