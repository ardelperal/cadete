    <h2>
        01. Auditoría Interna: Resultados y mejoras                        
    </h2>

</div>

<p> 
    Como sabéis, los días 14, 15 y 16 de febrero tuvo lugar la
    Auditoría Interna PECAL para las jurídicas TDE y TSOL.
</p>

<p>
    Los resultados obtenidos fueron los siguientes:
</p>

<h3>NO CONFORMIDADES: 4</h3>

<ul class="list--dots">

    <li> 
        1. Falta de concordancia entre la planificación definida en el
        documento Plan de Gestión de Proyecto y el cumplimiento real de hitos
        en cuanto a fechas planificadas. 
    </li>
    <li> 
        2 No realización de Revisión de Oferta Económica por parte de
        calidad antes de su presentación al cliente. 
    </li>
    <li> 
        3. Ausencia de solicitud de los riesgos del proyecto a los proveedores.
    </li>
    <li> 
        4 Falta de planificación y realización de las actividades
        indicadas en los Planes de Gestión de Configuración de los
        proyectos.
    </li>

</ul>

<div class="centrado">
    <img src="/build/img/com/CDQ-23-001/1.jpg" style="height:300px">     
</div>

<p>
    Seguro que en este momento te estarás preguntando...
</p>

<div class="centrado">
    <p>
        ¿Qué puedo hacer para que estas no conformidades no vuelvan a repetirse?
    </p>
</div>

<p>
    Te dejo varios tips para evitar estas no conformidades:
</p>

<ul class="list--dots">

    <li> 
        Revisa el Plan de Gestión de tu proyecto
        comparando el seguimiento del proyecto y los hitos
        con lo definido en el plan. Replanifícalo si es
        necesario.
    </li>
    <li> 
        Envía siempre la oferta económica junto con la
        oferta técnica para que pueda revisarla calidad
        antes de enviarla al cliente. 
    </li>
    <li> 
        Comprueba que tienes los riesgos de los
        proveedores de tu proyecto y en caso de que no los
        hayas solicitado, hazlo cuanto antes.
    </li>
    <li> 
        Observa el Plan de Gestión de Configuración de tu
        proyecto para replanificar y realizar las actividades
        definidas en el mismo.
    </li>

</ul>

